package string_programs;

public class stringContains {

	public static void main(String[] args) {
		String a ="Java is programming language";
		System.out.println(a.contains("Java is"));
		System.out.println(a.contains("java is"));
		System.out.println(a.contains("Python is"));
		System.out.println(a.contains("programming language"));

	}

}
